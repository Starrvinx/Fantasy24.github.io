<div class="History-h2"> 
<h2>History</h2>
</div>

<div class="Histroy">
<img src="images/notch.png" alt="Notch">
    <p>
        Minecraft is a groundbreaking sandbox video game that allows players to create, explore, and survive in an expansive, block-based world limited only by their imagination. It was originally developed by Swedish programmer Markus Persson, better known as Notch, and released to the public as a developmental alpha on May 17, 2009. The game's simple yet powerful mechanics captured the attention of gamers worldwide, sparking a rapidly growing community.
    </p>

    <p>
        Following its early success, Minecraft was officially launched on November 18, 2011, by Mojang, a game studio founded by Persson. The game continued to evolve through frequent updates and community-driven content, becoming one of the most influential titles in gaming history. In September 2014, Mojang was acquired by Microsoft for $2.5 billion, further expanding Minecraft's reach and support across platforms such as Windows, macOS, Xbox, PlayStation, Nintendo Switch, iOS, and Android.
    </p>
    <p></p>
</div>

